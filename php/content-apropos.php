						<h3>C'est quoi un LAN?</h3>
						Un LAN Party (aussi appelé LAN) est tout simplement un évènement ou des joueurs se rassemblent dans le but de jouer à des jeux vidéos en étant connectés sur le même réseau local - d'où le terme LAN, qui provient du concept de Local Area Network, ou réseau local. Certains évènements du genre, tels que l'ÉTICS LAN, sont orientés vers les tournois de jeux vidéos, où plusieurs équipes s'affrontent afin de gagner des prix.
